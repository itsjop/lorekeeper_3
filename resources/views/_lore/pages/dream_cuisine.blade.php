<h1 id="-processed-dreams-"><strong>Processed Dreams</strong></h1>
<h1 id="-">---</h1>
<h2 id="-dream-brews-"><strong>Dream Brews</strong></h2>
<p>Given that live feeding is not a consistently available option for somnivores in Reverie, dream brews are usually a somnivore’s
  go-to for fulfilling their daily dream intake. These bottled dreams have a much longer <strong>shelf life</strong> compared to
  fresh dream essence (which has virtually none). Unfortunately, the specific contents of the dreams fade away with age, leaving
  only their signature flavor and a gentle stir of emotion.</p>
<p>![][image2]</p>
<p>Dream brews often use special mana-infused ingredients to punch up their flavors or to maintain emotional potency for extended
  periods of time. It’s an art form that many somnivores are dedicated to. These specialty brews are often sold in their own
  custom glass vials - containers just as unique as the concoction inside.
  ![][image3]
  Artisan brews can be a bit pricey for everyday fare though, which is where <strong>mass-produced</strong> dream brews come in!
  These are typically contained in simple glass or plastic bottles adorned with flashy labels that advertise their “nutritional
  benefits”. The flavors aren’t nearly as nuanced as an artisan brew’s, but they get the job done.</p>
<p>R.E.M. is the largest company that mass produces these drinks thanks to their innovations in dream harvesting and processing
  technology. If you would like to see “how it’s made” be sure to schedule a tour of their factories in the Industrial District!
</p>
