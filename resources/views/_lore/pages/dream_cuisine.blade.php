<h2 id="-hunting-on-earth-"><strong>Hunting on Earth</strong></h2>
<p>Hunting on Earth must be done with the utmost caution to avoid being caught. Somnivores that wish to feed fresh from humans or
  take up Dream Harvesting must pass an exam to become certified.
  The exam for a <strong>Dream Hunter’s License</strong> tests for:</p>
<ul>
  <li>Mastery of invisibility</li>
  <li>Flight maneuverability</li>
  <li>Portal Channeling</li>
</ul>
