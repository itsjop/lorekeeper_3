<h1 id="-other-foods-their-industries-"><strong>Other Foods &amp; Their Industries</strong></h1>
<hr>
<p>Somnivores also enjoy eating foods outside of dream essence - it lets them experience different textures and flavors they don’t
  traditionally get from downing their daily dream brew. However, non-dream foods are purely a creature comfort and don’t provide
  any nutritional value (unless they’re prepared with essence concentrate).</p>
<p>Somnivores’ favorite foods align with those of real world bats:</p>
<ul>
  <li>Bugs &amp; Fish</li>
  <li>Fruits &amp; Berries</li>
  <li>Nuts &amp; Seeds</li>
  <li>Flowers &amp; Nectar</li>
</ul>
<p>![][image9]</p>
<p>The industries that provide these foods are relatively small, existing only for somnivores’ culinary enrichment and Reverie’s
  non-somnivore population.</p>
<p>Any produce that can be grown and cared for in large quantities like this would not offer any significant magical properties
  like their wild-grown counterparts. Regardless, these blooms, berries, and bugs will surely have you coming back for seconds!
</p>
<h2 id="-horticulture-"><strong>Horticulture</strong></h2>
<p>Horticulture farms cultivate various plant-based foods for consumption. Primary crops include vegetables, fruits, nuts, and
  berries.</p>
<ul>
  <li>Harvest Moons Farm - Located in Dream Haven</li>
</ul>
<h2 id="-floriculture-"><strong>Floriculture</strong></h2>
<p>Floriculture hubs focus on growing edible flowers with enhanced size, texture, and flavor. Nectar is also collected and sold at
  high prices.</p>
<ul>
  <li>Mellow Meadows - Located in Dream Haven</li>
</ul>
<h2 id="-insect-husbandry-"><strong>Insect Husbandry</strong></h2>
<p>This husbandry involves raising and caring for different kinds of insects to increase their meatiness, juiciness, and/or
  crunchiness.</p>
<ul>
  <li>The Grub Bubs - Located in Grimhallow</li>
</ul>
<h2 id="-aquaculture-"><strong>Aquaculture</strong></h2>
<p>A small industry that provides Reverie with fresh fish, crustaceans, and some aquatic plants.</p>
<ul>
  <li>Eventide Fishery - Located in Lullabay</li>
</ul>
<h2 id="-earth-imports-"><strong>Earth Imports</strong></h2>
<p>For any produce needs that can’t be met by Reverie’s farms or environment, there’s always the option to import from Earth.
  Witches facilitate these exchanges, trading bulk goods from human society in exchange for magic plants and artifacts from
  Reverie; the exchange rate is quite good, since Earth produce is much more abundant in comparison.</p>
