<h1 id="-eating-dreams-"><strong>Eating Dreams</strong></h1>
<h1 id="-">---</h1>
<h2 id="-traditional-hunting-"><strong>Traditional Hunting</strong></h2>
<p>Somnivores use their exceptional hearing abilities to find a dreamer by focusing on the sounds associated with sleep (i.e.
  snoring, deep breathing, sheets rustling). Once a somnivore has located their prey, they will settle around the dreamer in
  whatever manner suits it - some like to perch upon headboards, while others may lay on a dreamer’s chest. The somnivore then
  slowly absorbs the dream from their host, breathing in or munching on the pillowy dream essence and feeling the dream’s contents
  <strong>play through their mind</strong> like a distant memory of their own. As the dream is consumed, the somnivore’s wings and
  horns will subtly glow brighter with the new fuel.</p>
<h2 id="-impact-on-the-dreamer-"><strong>Impact on the Dreamer</strong></h2>
<p>The dreamer will not be able to recall their dream upon waking. This can be seen as a benefit to some and a loss to others,
  depending on the dream’s contents. Being unable to remember your dream is a common phenomenon; it could be because your dream
  became a somnivore’s meal. 🦇</p>
<p>There are no other <em>immediate</em> effects besides this small lapse in memory; however, if a person has their dreams eaten
  too regularly it may begin to take a toll on their mind. REM sleep is essential for memory, learning, and emotional processing.
  Without this crucial cognitive function, dreamers may experience the following symptoms after having their dreams consumed for
  several consecutive nights:</p>
<ul>
  <li>Brain Fog &amp; Memory Problems</li>
  <li>Increased Irritability</li>
  <li>Cognitive Impairment</li>
  <li>Auditory and Visual Hallucinations</li>
</ul>
<h1 id="-live-feeding-"><strong>Live Feeding</strong></h1>
<h1 id="-">---</h1>
<p>A fresh meal directly from the source is a delicacy for somnivores. Many rely heavily on the convenience of bottled dream
  brews, never actually needing to hunt for their sustenance.</p>
<h2 id="-feeding-in-reverie-"><strong>Feeding in Reverie</strong></h2>
<p>Dreaming residents of Reverie typically allow their dreams to be eaten - they can visit their local community center and fill
  out paperwork to be entered into the Registry of Dreamers. Enrollment rewards a monthly monetary compensation and can be opted
  out of at any time.</p>
<p>Registered dreamers are given a sticker to place on their bedroom window - if that window is left open, a somnivore is welcome
  to swoop inside and enjoy a fresh meal. Dreamers are encouraged to donate during 7 to 15 non-consecutive nights per month.</p>
