<h1 id="-dream-collection-"><strong>Dream Collection</strong></h1>
<hr>
<h2 id="-dream-harvesting-"><strong>Dream Harvesting</strong></h2>
<p>Harvesters are vacuum-like machines used to collect and condense dream essence for later consumption.
  They intake clouds of dream essence and process them through a cooling chamber to condensate. This turns the gas into liquid,
  which can then be used for raw consumption, or for brewing dream potions.</p>
<p>Harvesters come in many shapes and sizes. The larger and more advanced models are used by professional Dream Collectors. They
  have the ability to sort dreams by type into separate chambers and have a large capacity.</p>
<p>Personal models are small and lightweight, typically only having one chamber. It’s ideal for taking a dream “to-go”.</p>
<p>![][image5]</p>
<p>##</p>
<h2 id="-sheep-sprites-"><strong>Sheep Sprites</strong></h2>
<p>The large majority of the dream essence in Reverie is collected by sheep sprites, a mystical species of lamb-like creatures who
  have been bred over centuries to harvest dream essence.</p>
<p>They operate similarly to bees that collect pollen - venturing out each night to search for dream essence, gathering it around
  their small bodies, and then returning to their pasture. Once home, the sheep sprites are sheared and the collected essence is
  delicately packaged and sent away for processing - otherwise, it would naturally dissipate.</p>
<p>The most well known sheep sprite farm is Starlit Acres, located in Dreamhaven!
