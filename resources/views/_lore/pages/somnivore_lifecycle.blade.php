<h2 id="-grades-8-10-ages-18-20-"><strong>Grades 8 - 10 (Ages 18 - 20)</strong></h2>
<p>Somnivores are able to take advanced courses for practical magic once their palate has fully cemented. This timeline varies
  since everyone reaches maturity at slightly different speeds, but is tracked by regularly monitoring a somni’s horn size and
  hue. When a somnivore’s horns haven’t changed for more than a month, it’s safe to say that their palate is finished cooking and
  they can begin learning magic!</p>
