<h2 id="-adults-"><strong>Adults</strong></h2>
<p>Somnivores reach full physical maturity around 18-20 years old. At this age, their palate has been fully cemented and does not
  (naturally) change over the course of the rest of their life, except in extenuating circumstances.</p>
<p>Now with full agency of their magic, mature somnivores are able to sprout wings, fly, and shapeshift into their demon form at
  will. They’re able to perform other kinds of magic as well, which can be explored in the <a href="?tab=t.cbp40x5udqni">Somnivore
    Physiology &amp; Magic</a> section!</p>
