<h2 id="-grades-4-7-age-14-17-"><strong>Grades 4 - 7 (Age 14 - 17)</strong></h2>
<p>These grades introduce more nuanced topics that delve into how somnivores function and navigate the world.</p>
<p><em>Courses include:</em></p>
<p><strong>Dream Chemistry</strong></p>
<ul>
  <li>Teaches somnivores how to identify the different types of dreams and how R.E.M sleep operates. Understanding how dream types
    are distinguished and the human emotions that define them is a crucial stepping stone in discovering their palate.</li>
</ul>
<p><strong>Biology &amp; Health</strong></p>
<ul>
  <li>Covers how somnivore bodies function, the importance of resonance and a healthy diet, and all about their lifecycle.</li>
</ul>
<p><strong>Magical Theory</strong></p>
<ul>
  <li>Teaches about different ways dream magic can be used. While somnivores are unable to actually <em>practice</em> magic at
    this age, it’s crucial that they have a thorough understanding before wielding it in adulthood.</li>
</ul>
<p><strong>Earth Education</strong></p>
<ul>
  <li>Educates somnivores about the differences between Reverie and Earth, and all of its dangers. This course covers dream
    collection methods and outlines the strict safety measures that are in place for Earth travel.</li>
  <li>Includes a somnivore’s first <strong>field trip to Earth</strong>! They spend three days visiting an allied witch coven’s
    commune. This also includes a stealthy visit to a major human city (with the help of flight and invisibility administered by
    experts).</li>
</ul>
