<h1 id="-reproduction-"><strong>Reproduction</strong></h1>
<hr>
<h2 id="-origins-"><strong>Origins</strong></h2>
<p>Somnivores evolved artificially, brought into the world by the desperate experiments of the <a
    href="?tab=t.n0x0zhintecy"><strong>Infernal Alliance</strong></a>. The main catalyst used for this evolution is called a
  <strong>soul prism</strong>; an arcane artifact smuggled from the underworld that contains pieces of fragmented souls. These
  pieces can be forged together to create a new life - the process that eventually gave rise to the dream-feeding hybrid species.
</p>
<p>Somnivores carry a complex heritage that combines the essence of multiple supernatural beings. Due to this hybrid nature, they
  are incapable of natural genetic reproduction or ‘turning’ others like their vampiric ancestors. Instead, they carry out a
  reproduction <strong>ritual</strong> that uses soul prisms, not unlike the process their ancestors went through to bring them
  into existence. This ritual has been refined through the generations, now a celebration of love and life instead of a desperate
  bid at survival.</p>
<p>Today, Reverie maintains formal trade routes and agreements with the demons of the Underworld to retain their access to soul
  prisms. Sanctioned expeditioners make the perilous journey into the depths of the Underworld once per year to secure this
  precious resource.</p>
<p>![][image1]</p>
<h2 id="-soul-prisms-"><strong>Soul Prisms</strong></h2>
<p>The natural state of a soul prism is volatile, fragile, and vaguely sinister. The fragmented soul pieces carry countless years
  of anguish and suffering experienced during their time in the Underworld - enough to finally reach their breaking point. These
  pieces aren’t sentient, but they remain incredibly receptive to <strong>energy</strong>.</p>
<p>With exposure to enough affectionate and harmonious energy, a soul prism’s inner disarray will fade away. The fragmented bits
  of souls are soothed by the warmth of a loving home, finally able to rest. Only then are they able to meld together and become
  the precious essence used to create a new life.</p>
<p>Once development is underway, the prism will softly glow and become warm to the touch as the somnivore inside begins to form.
  There is no linear timeline for when a newborn will crack out of its prism. It happens when the crystal has become brittle, all
  of its strength now belonging to the new life within. The frail outer shell will crack and fall away to reveal a tiny newborn
  somnivore pup!</p>
<h2 id="-ritual-"><strong>Ritual</strong></h2>
<p>While it’s called a ritual, there isn’t really a whole lot that needs to actually be <em>done</em> by the somnivores. It’s just
  like incubating an egg, but the growth is generated by love instead of heat.</p>
<p>The prism is nestled into a handcrafted nest and placed in the heart of the home. This nest is constructed out of materials and
  items that carry <strong>sentimental value</strong>, helping jumpstart the prism’s cleanse. It’s also crucial that the nest
  contains genetic material from two parents - this is usually fur, since it’s soft and looks much nicer than the alternatives.
</p>
<p>All sorts of pairs can foster a soul prism - whether their relationship is platonic or romantic. Polyamorous or communal
  households may also carry out the ritual, though the offspring will ultimately only pull genetic influence from the two members
  that constructed the nest - introducing any extra “parents” increases the risk of an unsuccessful soul formation.</p>
