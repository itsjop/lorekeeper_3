<h2 id="-pups-"><strong>Pups</strong></h2>
<p>Pups are just teeny tiny babies. They crawl around on all 4 paws. Their ears are a bit rounded and floppy and their tail is
  stubby and tuftless. Somnivore pups have no horns or wings, only tiny horn nubs that are just starting to show. Pups have a diet
  of only mundane dreams that have been diluted with fresh spring water.</p>
<p>##</p>
<h2 id="-fledglings-"><strong>Fledglings</strong></h2>
<p>Fledglings are the somnivore equivalent to human children. At this stage they’re still plump with their baby fat and are
  learning to walk on two legs. Their ears are a bit larger and pointier, their nubs have fully developed, and they’ll begin to
  grow a tuft of fur on the tip of their tail. Fledglings still have no horns or wings at this stage, and still follow a strict
  diet of only mundane dreams.</p>
<p>##</p>
<h2 id="-adolescents-"><strong>Adolescents</strong></h2>
<p>Somnivores really begin to come into their own during the adolescent stage. Their bodies are similar to an adult’s in shape,
  only slightly smaller in size. They’re mature enough to begin trying different types of dreams, though they are diluted to ease
  the transition. As they discover which dreams resonate with them, their horns slowly begin to form around their nubs. These
  horns are always small at first, and the colors inside are dull and may fluctuate as they discover which palate best suits them.
  Due to the transient nature of their palate during this stage, adolescent somnivores are still unable to use magic and remain
  wingless.</p>
