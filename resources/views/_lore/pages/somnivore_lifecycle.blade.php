<h1 id="-early-development-education-"><strong>Early Development &amp; Education</strong></h1>
<hr>
<h2 id="-it-takes-a-colony-"><strong>It Takes a Colony</strong></h2>
<p>Somnivores have a strong sense of community, much like real world bat colonies do. Parents often share the responsibilities of
  raising pups with the members of their neighborhood. Parents often take turns watching over multiple pups, and sometimes pupless
  somnivores enjoy helping as a change of pace. This allows parents a much needed reprieve, and allows pups to have a healthy
  diversification in their socialization.</p>
<p>Even though pups might spend time under several different roofs, they always have a warm meal and bed waiting for them at their
  parents’ home.</p>
<h2 id="-daycare-"><strong>Daycare</strong></h2>
<p>Daycare is also an option for young somnivores, providing them with a safe space to play and socialize with their peers. These
  spaces are full of fun and colorful sensory toys and imaginative playsets for playing pretend.</p>
<h2 id="-schoolhouses-"><strong>Schoolhouses</strong></h2>
<p>At age 6, all somnivores must be enrolled at the closest school house, which they attend 4 days a week until they turn 10.
  These schools shallowly cover a wide variety of topics, mainly serving as a place to foster curiosity and desire for knowledge.
  Some believe it&#39;s more of a glorified daycare than an educational institution, but many somnivores look back at these years
  fondly.</p>
<p>##</p>
<h1 id="-reverie-academy-"><strong>Reverie Academy</strong></h1>
<hr>
<p>At age 10, somnivores are whisked away to Reverie Academy; a whimsical boarding school where they begin learning a proper
  curriculum. After enrollment, students spend the next decade learning everything from the art of magic, history of the world,
  and the important tasks of everyday life.</p>
<p>Even though students live in campus dorms, there are plenty of holidays and extended weekends throughout the year when they’re
  able to return home and visit with family and friends.</p>
<h2 id="-grades-1-3-age-10-13-"><strong>Grades 1 - 3 (Age 10 - 13)</strong></h2>
<p>These grades establish many classical subjects, including math, literature, art, science (&amp; magic), and history. Somnivores
  continue learning all of these subjects at higher levels in later grades, but these years establish the basics.</p>
<p>Socially, somnivores are encouraged to participate in at least one of the many clubs and extracurriculars that Reverie Academy
  offers. Some examples include the Crafter’s Colony, the Grub Club, and the Somnivore-Human Alliance (SHA).</p>
