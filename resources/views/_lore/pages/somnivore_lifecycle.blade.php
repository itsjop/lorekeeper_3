<h2 id="-graduation-"><strong>Graduation</strong></h2>
<p>After successfully honing the basics of magic, somnivores are allowed to (literally) spread their wings and soar into the next
  chapter of their life. Some may choose to pursue higher education at Somniversity, while others prefer to find a place of their
  own and some modest work that they enjoy. Whichever path a somnivore chooses, their Reverie Academy diploma proves that they’re
  ready to take it on!</p>
