<h1 id="-colony-community-"><strong>Colony Community</strong></h1>
<h1 id="-">---</h1>
<p>Community is one of Reverie’s core values. Somnivores naturally form colonies; close knit groups that comfortably lean on one another for support. When everyone helps out, everyone can spend more time taking it easy - a somnivore’s favorite thing to do. As a result, residents of Reverie have a reputation for being largely helpful and kind.</p>
<h2 id="-events-"><strong>Events</strong></h2>
<p>The community celebrates <em>numerous</em> events and holidays throughout the year, to the point where it sometimes seems a bit excessive. Somnivores will truly take any excuse to unwind, party, and spend quality time with their friends and neighbors.</p>
<p>Reverie’s event planners always go all out, however silly the occasion may be. Each event is hosted in one of Reverie&#39;s major towns and cities, allowing coordinators  to completely transform the space into a spectacle tailored to the celebration’s theme (with extra accommodations for the influx of guests, if needed). These celebrations never fail to bring the whole realm together, also giving residents an excuse to travel and appreciate Reverie’s diverse landscape.</p>
