<h2 id="-sex-"><strong>Sex</strong></h2>
<p>Biologically, somnivores are sexless - they cannot reproduce sexually and thus have no sexual characteristics. In their demon form, somnivores may choose to represent human-inspired sex and gender according to their preference. These characteristics are fluid, and a somnivore may even have multiple demon forms that present differently in terms of gender, sex, and/or body type.</p>
<p>In any form, somnivores typically choose to define their preferred pronouns rather than prescribe to a specific gender identity, but this is ultimately a personal decision.</p>
<p>Much like humans, somnivores experience a wide variety of different attractions and desires in ways that are unique to each individual - there is no right or wrong way, and no stigmas towards different relationship styles in Reverie society.</p>
<h2 id="-relationships-"><strong>Relationships</strong></h2>
<p>Many somnivores desire romantic relationships with others; attending mixers, dating around, and even settling down with their perfect match. Given their long lives, somnivores often experience some level of fluidity in their relationship dynamics. Open relationships and polycules are common, though monogamous pairs aren’t unheard of.</p>
<p>Marriage isn’t an officially recognized status, but somnivores may use the terms husband, wife, or partner to denote a long term commitment. However, if things don’t work out, somnivores often opt for keeping breakups amicable over burning bridges due to the close knit nature of their community.</p>
