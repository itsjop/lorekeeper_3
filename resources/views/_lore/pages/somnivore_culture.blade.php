<h2 id="-work-"><strong>Work</strong></h2>
<p>Somnivores have an extremely laid-back nature and greatly value their leisure time. Reverie’s society is structured in such a way that no one needs to work particularly hard to make ends meet; work weeks are four days and vacation time is plentiful. Gig work is also a common route, as there’s no shortage of odd jobs to be done around Reverie.</p>
<p>![][image2]</p>
<p>Admittedly, the realm has its fair share of unemployed residents that are welcome to lean on the community for support. Spending some time without being in schooling or a career is socially acceptable, and a period that many somnivores will go through at some point during their incredibly long adult life.</p>
