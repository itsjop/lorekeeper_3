<h1 id="-magitech-"><strong>Magitech</strong></h1>
<hr>
<h2 id="-energy-power-"><strong>Energy &amp; Power</strong></h2>
<p>Reverie’s magic-fueled electronics are collectively referred to as <strong>Magitech</strong>. Since mana is a versatile and
  plentiful resource in Reverie, it makes the perfect energy source to power all sorts of devices. Driven by their love of
  leisure, somnivores place a huge emphasis on innovation and automation, leaning heavily on magitech to make their lives easier.
</p>
<p>Devices are typically designed to be wireless, using chunky glowing batteries of highly concentrated mana to power them; you
  can actually see the amount of juice left through their transparent casing. Recent advancements in magictech even allow for
  low-power devices (like clocks, night lights, and e-readers) to run with no power source at all; instead passively absorbing
  mana from the air itself. High-power devices, however, may require a direct connection to a mana source via thick flexible PVC
  tubes, not unlike those used for water cooling inside PCs.</p>
<h2 id="-modern-advanced-technology-"><strong>Modern &amp; Advanced Technology</strong></h2>
<p>The technology itself is generally on-par with modern day Earth’s, though it lags behind and advances ahead in different areas.
  The internet landscape and personal device market are comparable to the early 2000s, while vehicles like scooters and trains can
  maneuver through the air freely in ways that humans have yet to perfect. Reverie’s engineers prioritize developing tech that’s
  <em>new</em> and <em>interesting</em> rather than making advancements in areas that they perceive as fulfilled - “if it ain’t
  broke, don’t fix it”.</p>
