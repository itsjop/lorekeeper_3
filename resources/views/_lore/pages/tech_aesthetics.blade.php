<h1 id="-architecture-"><strong>Architecture</strong></h1>
<hr>
<p>Thanks to somnivores’ magic-aided construction methods, structures in Reverie are wholly unique and can be quite the spectacle.
  Resident architects love to utilize asymmetry, curved edges, and polychromy in their designs. Buildings are colorful and
  organic, like something plucked straight from a dream.</p>
<p>Many constructs are built with Reverie’s wide variety of residents in mind; accommodating for all sorts of shapes, sizes, and
  mobility levels. Large structures almost always include towers, landing balconies, and high vaulted ceilings for the ease of
  flying bat form somnivores.</p>
<p>Suburban and rural areas feature more humble architecture, though still with their own whimsical flare. Flourishes can be seen
  in their large windows, cozy balconies, and gabled roofs.</p>
