<h2 id="-demon-form-"><strong>Demon Form</strong></h2>
<p>Pulling inspiration from the dreams they eat, somnivores have an extremely diverse fashion scene. Styles are limited only by
  imagination itself, spanning across all time periods and aesthetics.</p>
<p>One thing they never compromise on, however, is comfort. Somnivores typically value being comfortable above being trendy, and
  wearing pajamas or nightclothes out and about is commonplace. What a somnivore finds most comfortable varies based on the
  individual; some prefer being buried by layers while others favor wearing nothing more than their undergarments. Regardless of
  style, clothes are always made with high quality fabrics that evoke feelings of luxury and relaxation when brushing against
  their fur or skin.</p>
