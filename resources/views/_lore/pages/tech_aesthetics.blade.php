<h2 id="-bat-form-"><strong>Bat Form</strong></h2>
<p>It’s a common form of self expression for somnivores to accessorize in their bat form. Their large horns serve as a canvas to
  decorate with rings, charms, ribbons, and more! Pieces are also commonly worn around their tails, necks, and feet/ankles.
  Wearing full garments like tops, dresses, and pants is a bit less popular; it’s important that clothing doesn’t restrict their
  bodies for ease of flight and maneuverability.</p>
