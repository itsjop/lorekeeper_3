<h2 id="-aesthetic-"><strong>Aesthetic</strong></h2>
<p>Reverie’s tech is as colorful and varied as its residents. Inspired by the playful aesthetics of Earth’s 90s and 00s, magitech
  is designed with unique shapes, tactile buttons, and transparent plastics. Glowing parts are also common, either by using small
  lights or by directly displaying the mana that courses through the device. This soft glow not only <em>looks</em> cool, but also
  helps with visibility in low-light conditions.</p>
<p>Drop protection is also an important angle for somnivores to consider, as holding things with paws (especially while flying)
  can be tricky. Many devices come with lanyard/charm loops, or spots for attaching straps. Thick casing and rounded shapes also
  help with damage reduction; these delightfully clunky gadgets are built to last.</p>
