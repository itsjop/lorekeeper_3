<h2 id="-consequences-"><strong>Consequences</strong></h2>
<p>Despite its many conveniences, magitech isn&#39;t without its flaws. The process used to siphon mana from Reverie’s air and
  water takes massive, complex machines. Once the mana has been extracted, the contraption excretes mysterious smog and sludge -
  the colors seemingly changing at random, shimmering with an odd oil-slick finish. Its properties are unstable, and scientists
  are still studying what effects this magic pollution could have on Reverie’s environment and wildlife.</p>
