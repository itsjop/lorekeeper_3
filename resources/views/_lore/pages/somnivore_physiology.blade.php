<h2 id="-pocket-dimension-"><strong>Pocket Dimension</strong></h2>
<p>Literally putting the pocket in pocket dimension!</p>
<p>All somnivores possess access to an individual microfold in space due to their reality warping abilities. This pocket dimension
  is “suitcase sized” in its storage capacity and most often used for tools, personalized items, or clothes needed for
  transforming into their demon form. It allows for the containment of physical, non-sentient matter, but cannot sustain anything
  with an active mind. Sentient beings interfere with the mini-dimensional’s cohesion, making storage dangerous or impossible.</p>
<p>Just as using obscuring mist during transformation is traditional, so is creating a tangible &quot;anchor&quot; to personalize
  and stabilize access. Instead of simply snapping their fingers to store a phone, many prefer to slip it into a special conduit
  like a “pocket-dimension purse” or customized container that serves as an access point.</p>
