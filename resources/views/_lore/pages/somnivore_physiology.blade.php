<h2 id="-reality-manipulation-"><strong>Reality Manipulation</strong></h2>
<p>Dream magic has reality-altering properties, especially in Reverie’s mana-rich atmosphere. All of a somnivore’s most impressive
  magical powers fall under this “reality manipulation” umbrella.</p>
<p>A single somnivore is able to perform small feats of reality manipulation on their own, such as light telekinesis or creating
  wispy apparitions. Invisibility can also be achieved by altering the air around them, but it requires unwavering focus to
  maintain. Mastery of invisibility is required to obtain a Dream Hunter’s License (along with other skills such as flight
  maneuverability and portal channeling).</p>
<p>Large-scale reality manipulation can be orchestrated by a group or <em>colony</em> of somnivores - this is referred to as a
  “Shared Dream”. This magic scales with the number of users, growing stronger each time the intent of the incantation is
  reinforced by another somnivore.</p>
<p>The original colony of somnivores used this power to create the first rift into Reverie. This method is also used to explore
  and expand new areas of Reverie; the landscape taking shape as a median of the group’s envisioned formation. On a smaller scale,
  teams of builders are able to use shared dreams to efficiently transform raw materials into Reverie’s various structures. A
  blueprint and exact materials are needed - using only imagination and estimations would be far too unstable for creating sound
  structures built to last.</p>
