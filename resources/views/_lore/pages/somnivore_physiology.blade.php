<h2 id="-sleep-"><strong>Sleep</strong></h2>
<p>Somnivores have the ability to <em>sleep</em>, but lack the capacity to <em>dream</em>. Since they’re unable to enter a deep,
  REM-filled slumber, they tend to nap in short intervals through the daylight hours. They will still typically accumulate around
  a total of six to eight hours of rest each day (even if their sleepy demeanor may suggest otherwise).</p>
