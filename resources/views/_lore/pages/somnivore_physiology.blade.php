<h2 id="-flight-"><strong>Flight</strong></h2>
<p>A somnivore’s wings are materialized using the dream essence they consume - it may take some practice to get the hang of
  navigating the air, but it&#39;s an ability all somnivores have. These wings are able to carry a somnivore’s body very long
  distances (regardless of their size) while only using a small amount of magical energy. Some flight fanatics are even able to
  expend extra magic for short bursts of blinding speeds!</p>
<p>While in their demon form, a somnivore may choose to materialize their wings as an aesthetic choice. They aren’t able to take
  flight, but can glide short distances if their wings are large enough.</p>
<p>##</p>
<h2 id="-healing-"><strong>Healing</strong></h2>
<p>Somnivores’ bodies passively use magic to heal most injuries and illnesses. Injuries that are able to be healed are mended
  quickly; about ten times faster than a human body would be able to.</p>
<p>Not all injuries are able to be healed, however - lost limbs are unable to grow back and broken horns or torn wings cannot mend
  on their own. Any injury that pierces their brain is fatal, as it is for most living beings.</p>
