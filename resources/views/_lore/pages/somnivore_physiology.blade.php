<hr>
<h2 id="-consumption-resonance-"><strong>Consumption &amp; Resonance</strong></h2>
<p>When a somnivore consumes dream essence, their body reacts differently depending on whether the type of dream is able to be
  metabolized and stored in the body, a biological process commonly referred to as <strong>resonance</strong>. This system of
  resonance is what allows a somnivore’s body to turn dream essence into fuel for their magic - and also defines their palate in
  the process.</p>
<p>That magical fuel is processed by the body and then stored inside a somnivore’s <strong>cerebro-spinal fluid (CSF).</strong>
  This goopy, dream essence-filled fluid surrounds a somnivore’s brain and spinal cord and connects to their horns and wings,
  which are materialized using the unique substance. Besides being a striking part of their appearance, a somnivore’s
  essence-filled horns serve as extra storage to ensure that their brain is never allowed to dry out. This storage is incredibly
  dense, horn size doesn’t actually influence capacity in any significant way. The CSF that suspends and surrounds the brain
  serves as the conduit for their magical abilities; it’s the catalyst that transforms thoughts (imagination with intent) into
  actual magic.
  <strong>![][image1]</strong>
  Somnivores are normally only suited to absorb and metabolize one to two types of dream essence without taking on a number of
  detrimental symptoms. Having off-palate, unprocessed essence in their system can induce a number of side-effects such as
  light-headedness, nausea, fatigue, and more - increasing in potency with the amount they’ve consumed. In extreme cases of
  disproportionately off-palate diets, the bare-minimum amount of resonant essence stays preserved in their skull, tasked only
  with keeping their brain in good health, at the cost of all usable magic.
</p>
<p>Thankfully dreams are abundant in Reverie, and somnivores are able to thrive with a plethora of palate appropriate meals to
  choose from.</p>
<h2 id="-starvation-"><strong>Starvation</strong></h2>
<p>Thanks to the aforementioned dream abundance, starvation is never seen in Reverie. However, the condition remains an important
  cautionary tale for modern somnivores to learn.</p>
<p>When facing starvation a somnivore will begin to turn bestial. Their horns will darken, their wings will retract, and they will
  begin to lose their higher functions as they become increasingly hunger-driven. They lose the ability to transform or perform
  any sort of magic, and their eyes glaze over. Starvation alone is not enough to kill a somnivore, but it weakens them to the
  point where they can no longer heal major injuries, which could ultimately lead to their demise.</p>
<p>Somnivores facing starvation are usually found stranded in unpopulated areas of Earth, or in infernal places such as Limbo or
  the Underworld where creatures are unable to sleep or dream. A starving somnivore can be rehabilitated, but will lack any
  memories from the time they were starved. The sustained brain damage is able to be mended, but leaves the past a splotchy blur
  of only feelings and flashes of color.</p>
