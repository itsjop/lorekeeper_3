<h2 id="-sleepy-aura-"><strong>Sleepy Aura</strong></h2>
<p>The presence of a somnivore is almost always accompanied by a deep, dream-filled slumber. They passively emit a soothing aura
  that increases melatonin production - some swear it also smells faintly of lavender, though the scent varies from somni to
  somni. This ability developed as an evolutionary advantage, allowing somnivores to more consistently secure a quality meal while
  also decreasing the chances of a dreamer stirring awake mid-feeding.</p>
<p>Beings that reside in Reverie full time thankfully develop a tolerance to this effect, allowing them to live among somnivores
  without constant brain fog and sleeping spells.</p>
