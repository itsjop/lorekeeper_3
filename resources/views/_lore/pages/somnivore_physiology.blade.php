<h2 id="-aging-"><strong>Aging</strong></h2>
<p>Somnivores age at a rate that is comparable to humans until they reach full maturity around the age of <strong>twenty</strong>.
  As they move into adulthood, the rate at which they age slows dramatically. While they have <em>very</em> long lives, somnivores
  were unfortunately unable to inherit true immortality from their ancestors. Old age has taken a precious few of the somnivore
  population in recent decades, the oldest colony members passing away at around <strong>three hundred</strong> years of age -
  though some have still surpassed this.</p>
<p>Scholars speculate that immortality was lost as a byproduct of their soul prism-based creation. Recycling old soul fragments
  surely has its side effects, and they’re only just now becoming known as the species enters its fourth century of existence.</p>
<p>Immortality may not be a part of their default toolkit, but that certainly doesn’t stop a few very determined bats from doing
  everything in their power to try and obtain it.</p>
<h1 id="-magic-"><strong>Magic</strong></h1>
<hr>
<p>Magic is performed through <strong>intent and imagination</strong> - channeling a thought and materializing it through the
  power of the dream essence that surrounds their mind. Some feats are easier to achieve than others; basic magic abilities allow
  for changes to the self such as sprouting wings and shapeshifting, while more advanced techniques can be used to alter reality
  itself.</p>
<h2 id="-shapeshifting-"><strong>Shapeshifting</strong></h2>
<p>Somnivores are able to shapeshift into a humanoid version of themselves referred to as their “demon” form. This transformation
  functions similarly to how their vampire ancestors also once freely shifted between bat and humanoid bodies. Which form a
  somnivore favors depends on their personal preference, but bat forms are always used for traveling and hunting.</p>
<p>Shapeshifting requires a somnivore to have full control of their magic; perfecting and maintaining a demon form isn’t feasible
  until adulthood. Many young somnivores daydream about their demonic visage, taking inspiration from the dreams they consume. It
  isn’t uncommon for a somnivore to decide what their demon form will look like long before they’re even able to pull off the
  transformation. Having multiple different demon forms is also possible, though it does take practice and a certain level of
  skill.</p>
