title 🌿 Drawing Reverie’s Scenery 🌙
blockquote Looking to draw an outdoor scene for a prompt?
  | Here’s a quick guide for how Reverie’s scenery differs from Earth’s!
img

h1 Sky
img
ul
 li Reverie has no sun.
 li There are 3 moons; two crescent moons that face towards a full moon.
  ul
    li The moons don’t need to be showing!
img
ul
  li Night is eternal in Reverie, the color of the sky should reflect this.
  li Please avoid using only a light shade of sky blue, as it makes it look like daytime.

h1 Plants
img
ul
  li Grass comes in many colors!
  li While a more traditional grass green is possible, teal is the most common grass color.
img
ul
  li Tree bark comes in cool hues of purples, blues, and grays.
    ul
      li No warm brown hues.
  li Foliage commonly takes on the same hues that Reverie’s grass does, but it can be any color!

img
ul
  li Examples of some of Reverie’s plant life &amp; fungi. Feel free to get creative when drawing flora!

h1 Water
ul
  li Most bodies of water have a blue hue, similar to Earth’s.
  li Water has star-like glitter flowing through it (mana traces).
    ul
      li Especially pronounced in the Celestial Sea, which looks like cosmos.
