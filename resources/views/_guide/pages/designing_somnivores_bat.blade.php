h1 Injuries &amp; Disabilities
p Injuries &amp; disabilities are not a part of the trait system and can all be applied to any somnivore character. Somnivores may have signs of past injuries. These injuries may not be self-inflicted and cannot be actively bleeding or healing. They can be bandaged over as an aesthetic choice.
p Somnivore characters may possess any mental or physical disabilities present in humans, but they must be represented mindfully and respectively. Any physically represented injury/disability should be consistent across all of a somnivore’s forms
img

h2 Scars
p It’s recommended (but not required) that scars be colored with the somnivore’s skin color (matching the inner ear).  Scars may not cover more than half of a somnivore's body.

h2 Tattering
p A somnivore’s wings may appear tattered. This should not heavily alter the overall silhouette of the wings; the original shape should still be able to be inferred.

h2 Damaged Horns
p Somnivores can have chips or cracks in their horns.One of a somnivore’s horns may be broken. For the sake of clarity, one horn should still be whole.
p Horn damage cannot cause dream essence to “leak” out of them.

h2 Amputations
p Somnivores may have amputated limbs; up to one arm and one leg.
p An amputated leg requires a prosthetic or mobility aid in any or all of a somnivore’s form

h2 Prosthetics &amp; Mobility Aids
p Prosthetics and mobility aids are allowed. They can be representative of the technology we have in real life, or they can be interpreted through a more imaginative lens, considering how they might look in Reverie. Regardless of how they look, they can’t give a somnivore any sort of extra powers or advantages.


h1 Prohibited Content
p There are a few restrictions for somnivore character creation; I’ll fine-tune/add to these in the future, but the key points to know are:
ul
  li No designs based on copyrighted characters that you do not have the rights to.
  li No hate symbols, gang visuals, and other similar imagery
  li No modern war paraphernalia [i.e., real guns, grenades, military uniforms]
  li No designs based on negative stereotypes related to race/disabilities/etc. “Personified” country designs are also not allowed.




