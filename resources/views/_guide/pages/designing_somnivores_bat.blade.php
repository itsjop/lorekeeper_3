h1 Body
p Somnivores have small bodies- their torso is typically around the same size as their head. A somnivore’s total height is roughly between 1’ - 1’6” [ 30cm - 45cm] , not including horns.
p They stand upright on powerful digitigrade hindlimbs with large feet. Their forelimbs are thick with large paws, but are notably smaller than their feet.

h2 Paws &amp; Feet
p Upper limbs can end in paws or claws, lower limbs can end in paws, claws, or hooves. Hooves can be any naturally-occurring shape; dew claws are optional. Paws can be drawn with 3 or 4 digits.
img
label In order: upper limbs: paws, claws.  lower limb: paws, claws, hooves

h2 Fur
p Somnivores naturally have short fur.  Short fluff can be drawn anywhere on their body, and lengthier fur is allowed on the
 span.bold.blue forehead and neck/chest
 | without any traits.
p Any areas besides these can be made extra fluffy with the
  span.bold.gold Winter Coat
  | trait.
ul
  li Fur is not allowed on the inner ear, even with the Winter Coat trait.
  li Fur originating from the forehead (bangs) can be any length. However, long fur or hair covering the whole scalp (ex. full human hairstyles) require the Winter Coat trait.
img
