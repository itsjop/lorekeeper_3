h2 Mouth &amp; Fangs
p All somnivores must have at least one fang. Variations include (but are not limited to) a single fang, vampire fangs, tusks, and shark teeth.
p If a somnivore is drawn with their mouth closed, these fangs do not need to be showing and will be implied to be the classic vampire style.
p The inside of the mouth &amp; tongue can be any color - the tongue can also be long or forked!
