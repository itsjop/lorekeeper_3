h2 Nubs &amp; Horns
p Somnivores are born with small nubs made of keratin. These serve as the foundation for their horns to form around when they reach maturity and their
  span.bold.blue palate
  | is solidified.
p A somnivore’s horns are their most distinctive feature. They’re made of the dazzling
  span.bold.blue dream essence
  | a somnivore consumes.
