h2 Wings
p A somnivore’s wings are able to form once they reach maturity. From this point forward, flight becomes a somnivore’s primary method of movement.
p Wings sprout from a shallow V-shaped groove on a somnivore’s back called their  “essence valve”. The dream essence filling this valve becomes flush with the back and then extends into wings
img
p By default these wings have a webbed (bat) shape, but other shapes are available with trait upgrades
p Wings are entirely made of dream essence. They should be drawn as a single continuous shape with no inner construction, details, or texturing besides the essence itself.
img
p Wings are able to carry a somnivore’s body regardless of their size; wingspan can range from only slightly wider than their body to roughly 2x their height (excluding horns).
