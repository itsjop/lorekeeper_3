<h1 id="-mystic-seasonal-traits-"><strong>💫Mystic &amp; Seasonal Traits</strong></h1>
<hr>
<p>How traits translate to a somnivore’s demon form is dependent on the type of trait and sometimes even the specific trait
  itself. <strong>Refer to the descriptions on the <a href="?tab=t.h85kwmervxtp">mystic traits page</a> to see the
    specifications</strong> of how each one is able to translate between forms.</p>
<p><strong>As a general rule; Any part of a demon that has fur coverage will express traits in the same way as their bat
    form.</strong></p>
<h2 id="-material-traits-"><strong>💫Material Traits</strong></h2>
<h3 id="-change-or-alter-the-materials-of-a-somnivore-s-body-"><em>Change or alter the materials of a somnivore’s body</em></h3>
<hr>
<p>Some material traits only translate on fur (i.e. clouds), while others can be present on skin as well (i.e. glass).</p>
<h2 id="-adornment-traits-"><strong>💫Adornment Traits</strong></h2>
<h3 id="-add-things-to-a-somnivore-s-body-"><em>Add things to a somnivore’s body</em></h3>
<hr>
<p>Adornment traits commonly translate their number and location directly from the bat form regardless of the presence of skin or
  fur (i.e. extra eyes, extra wings).</p>
<h2 id="-aura-traits-"><strong>💫Aura Traits</strong></h2>
<h3 id="-modify-the-area-around-a-somnivore-"><em>Modify the area around a somnivore</em></h3>
<hr>
<p>Aura traits always translate; though some are more flexible in how they’re represented (i.e. stars around a somnivore can be in
  the same general area and don’t necessarily need to have the exact same amount as the bat form).</p>
<h1 id="-limitations-"><strong>Limitations!</strong></h1>
<hr>
<p>Limitations in place for all demon forms.
  <strong>💫 Proportions</strong>
  Demons are similar in build to humans, but their adult heights can range from <strong>3’6” to 8’</strong> [106cm to 243cm ]
</p>
<p><strong>💫 Age</strong>
  All somnivores on the masterlist are adults. They can be petite or short but cannot appear to be a child. The canonical age of
  your somnivore can range from 20 to 300 years old.</p>
