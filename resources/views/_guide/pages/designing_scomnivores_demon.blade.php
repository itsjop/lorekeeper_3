<h2 id="-digitigrade-legs-"><strong>✨Digitigrade Legs</strong></h2>
<p>A demon form can have <strong>humanoid or digitigrade legs</strong>.</p>
<p>If a somnivore has digitigrade legs, the foot type must match their bat form and a <strong>fur gradient</strong> is required.
  This gradient can end anywhere above the ankle depending on fur coverage preferences</p>
<p>![][image12]</p>
<p>Digitigrade legs have lots of options for accessorizing!</p>
<p>Pawed feet are able to wear socks and shoes outfitted for digitigrade legs. However, hooved or clawed feet must <strong>keep
    the hooves/claws exposed</strong> and not completely covered by a sock or shoe.</p>
<p>![][image13]</p>
<h2 id="-paw-pads-claws-"><strong>✨Paw Pads &amp; Claws</strong></h2>
<p>Demons can have paw pads that match their bat form’s.</p>
<p>If a somnivore’s bat form has <strong>clawed upper paws</strong> then the demon form may possess clawed fingers. These are
  different from regular nails - claws are more talon-like and are (optionally) able to blend seamlessly with the fingertips.</p>
<p>![][image14]</p>
<h2 id="-markings-"><strong>✨Markings</strong></h2>
<p>Markings may be translated from their bat form onto their demon form. These don’t need to be 1:1 and can have changes that
  would make sense with the change in scale.</p>
<p>Demons can also have tattoos/markings that are not present on their bat form.</p>
<p>Markings and tattoos can cover up to 50% of a somnivore’s body.</p>
<p>![][image15]</p>
<h2 id="-limb-gradients-"><strong>✨Limb Gradients</strong></h2>
<p>Somnivores may have gradients covering up to 3/4ths of their limbs - this gradient color should correspond to the color(s) on
  their bat form.
  ![][image16]</p>
<h2 id="-fur-coverage-"><strong>✨Fur Coverage</strong></h2>
<p>At minimum, <strong>fur is required for any anatomy that has been translated from a somnivore’s bat form.</strong>
  The transition from skin to fur can be a gradient or drawn with a fur edge.</p>
<ul>
  <li>This includes bat ears, bat nose, digitigrade legs, and claws</li>
</ul>
<p>Fur coverage beyond this is allowed, the only restrictions being that skin must remain visible on the lower half of the face*,
  a small area of the chest, and the palms of the hand.
  <em>![][image17]</em>
  <em>\</em>Beards are allowed and would have the same coverage as on a human face.*
</p>
