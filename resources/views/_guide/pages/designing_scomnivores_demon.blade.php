</p>
<p>Different demon forms can differ greatly from each other! They can express optional attributes differently (ex. one with wings
  and one without) and can f<strong>reely change their build/gender presentation</strong> since it’s not tied to their bat form.
</p>
<h1 id="-required-elements-"><strong>🌟Required Elements</strong></h1>
<hr>
<p><strong>Elements of a demon’s design that are always translated from their bat form.</strong>
  ![][image3]
  Examples of two demon forms that only possess the required traits.</p>
<h2 id="-horns-"><strong>🌟Horns</strong></h2>
<p>Same as their bat form. They must not change in relative size, angle, or placement.</p>
<h2 id="-tail-"><strong>🌟Tail</strong></h2>
<p>Same style as their bat form but the length scales with their height - typically ending around or just past their ankles.</p>
<p>Fur covering the <strong>thin</strong> part of a common somnivore’s tail is optional- it can be covered with skin or fur until
  it reaches the tip, or any gradient in-between</p>
<h2 id="-skin-color-"><strong>🌟Skin Color</strong></h2>
<p>A demon’s skin color must mirror the primary color of the bat form’s <strong>inner ear</strong>. Any skin color is possible,
  though cool colors tend to be the most common.
  ![][image4]</p>
<h2 id="-pointed-ears-"><strong>🌟Pointed Ears</strong></h2>
<p>By default, a demon’s ears are humanoid and pointed (elf-like) and match the angle &amp; sizing of their bat form.
  ![][image5]</p>
<h2 id="-hair-color-"><strong>🌟Hair Color</strong></h2>
<p>A demon’s hair color reflects the fur color(s) of their bat form.</p>
<p>The main (root) color of the hair must correspond with the fur <strong>on top of a somnivore’s head</strong>, or the
  <strong>tuft of fur on their forehead</strong> if they have one.</p>
<p>Accent colors can be added based on other colors present in a somnivore’s coat, but aren’t required.</p>
<p><em>![][image6]</em>
  📋Due to the nature of a somnivore’s shapeshifting ability, cosmetic changes like hair dye would not persist between form
  changes. If a somnivore wants to sport a different hair color, they would instead get the color of their bat form’s coat
  altered!</p>
<p>#</p>
<h1 id="-optional-elements-"><strong>✨Optional Elements</strong></h1>
<hr>
<p><strong>These can be included to add more elements that harken back to the bat form’s original design.</strong>
  <strong>Mix and match these as desired!</strong>
  ![][image7]
  Examples of two demon forms that possess required &amp; optional traits.
</p>
<h2 id="-bat-ears-"><strong>✨Bat Ears</strong></h2>
<p>Bat ears are taken directly from a demon’s bat form. These ears always <strong>connect on the sides of the head</strong> where
  a human’s ears would be regardless of their placement on the bat’s head.</p>
<p>Floppy bat ears may not be longer than a demon’s waist.</p>
<p>![][image8]</p>
<h2 id="-bat-nose-"><strong>✨Bat Nose</strong></h2>
<p>Demons with bat noses possess a <strong>wide and flat nose bridge</strong>, ending with a wet nose tip that mirror’s their bat
  form’s.</p>
<p>A bat nose is always accompanied by <strong>facial fur</strong>, which covers the forehead and nose bridge.</p>
<p>![][image9]</p>
<p>![][image10]
  Nose anatomy / Side-view</p>
<h2 id="-wings-"><strong>✨Wings</strong></h2>
<p>Must be the same shape as in their bat form, but placement and size may vary. Wings can be detached (hovering behind the torso)
  or attached to the lower or upper back.</p>
<p>Wings cannot be larger than a demon’s torso.</p>
<p>![][image11]</p>
