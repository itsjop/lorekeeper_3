h1 Incorrect Somnivore Anatomy
The main thing to keep in mind is that a somnivore’s build cannot be too humanoid or anthropomorphic.
ul Somnivores:
  li Do not have defined necks, shoulders, chests/waists, or wrists.
  li Do not have legs that are plantigrade or elongated to define their thighs/knees.
  li Cannot be more than 3 heads tall.
  li Do not have visible breasts, nipples, or genitals.
