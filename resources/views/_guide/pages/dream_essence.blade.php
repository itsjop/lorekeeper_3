h1 Gradient
p Gradients of color can be drawn in different directions and orders, but they should not be used to “shade” the horn or wings. Dream essence emits light, so it should not have shading applied to its form.
img

h1 Stars
p Stars are a required element of dream essence. They can come in many many different shapes! They can also be any color, so long as it doesn’t heavily obscure the dream essence’s base color.
img

h1 Clouds / Nebula
p This element is optional, but can add depth &amp; texture to your dream essence.

h1 Transparency &amp; Light
p Dream essence is not fully transparent. A somnivore’s nubs can always be seen inside the horns, but essence is opaque at its full density.
p Slight transparency may be shown at the thinnest parts if desired (tips of horns/wings).
p Dream essence emits a soft light; you can choose whether or not to represent this in your art/scenes.
img

h1 Additional Notes
p The surface of a somnivore’s horns and wings feel like glass to the touch. You may choose to depict this surface layer, but the essence inside needs to fill it completely- it can not appear to be a liquid.
img
