title 📝 Drawing Dream Essence ✨
p A somnivore’s horns and wings are made of dream essence. The colors are indicative of the types of dreams they eat, or their palate. Refer to the
  a Palate Guide
  | to see all the subtypes &amp; color combinations!
p Please note that the fundamentals of dream essence can not be altered by any traits. It must always be drawn with the required elements.
h1 Color
p The color(s) you choose for your dream essence depend on which type of palate your somnivore has.
p The
  a palate guide
